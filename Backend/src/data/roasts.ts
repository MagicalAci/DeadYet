export function getCheckInResponse(hour: number): string {
  let responses: string[]
  
  if (hour >= 0 && hour < 6) {
    responses = checkInResponses.overnight
  } else if (hour >= 23) {
    responses = checkInResponses.lateNight
  } else if (hour >= 21) {
    responses = checkInResponses.heavyOvertime
  } else if (hour >= 19) {
    responses = checkInResponses.lightOvertime
  } else if (hour >= 18) {
    responses = checkInResponses.normal
  } else if (hour >= 17) {
    responses = checkInResponses.earlyBird
  } else {
    responses = checkInResponses.normal
  }
  
  return responses[Math.floor(Math.random() * responses.length)]
}

